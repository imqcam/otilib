void onumm20n1_div_ro_to(coeff_t num, onumm20n1_t* den, onumm20n1_t* res){

    onumm20n1_t inv;
    onumm20n1_pow_to( den, -1, &inv);
    onumm20n1_mul_ro_to(num,&inv,res);

}
// ----------------------------------------------------------------------------------------------------

// ****************************************************************************************************
void onumm20n1_div_oo_to(onumm20n1_t* num, onumm20n1_t* den, onumm20n1_t* res){

    onumm20n1_t inv = onumm20n1_init();
    onumm20n1_pow_to( den, -1, &inv);
    onumm20n1_mul_oo_to( num, &inv, res);

}
// ----------------------------------------------------------------------------------------------------

// ****************************************************************************************************
void onumm20n1_div_or_to(onumm20n1_t* num, coeff_t val, onumm20n1_t* res){

    onumm20n1_mul_ro_to(1.0/val, num, res);

}
