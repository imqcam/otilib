  ELEMENTAL SUBROUTINE MDNUM3_ASSIGN_R(RES,LHS)
    
    IMPLICIT NONE
    REAL(DP), INTENT(IN) :: LHS 
    TYPE(MDNUM3), INTENT(OUT) :: RES 

    ! Assign like function 'LHS'
    ! Real
    RES%R = LHS

    ! Order 1
    RES%E1 = 0.0_DP
    RES%E2 = 0.0_DP
    RES%E3 = 0.0_DP

    ! Order 2
    RES%E12 = 0.0_DP
    RES%E13 = 0.0_DP
    RES%E23 = 0.0_DP

    ! Order 3
    RES%E123 = 0.0_DP

  END SUBROUTINE MDNUM3_ASSIGN_R

