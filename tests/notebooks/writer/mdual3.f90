  SUBROUTINE MDNUM3_PPRINT_M_R(X, FMT)
      IMPLICIT NONE
      REAL(DP), INTENT(IN) :: X(:,:)
      INTEGER :: I, J
      CHARACTER(*), INTENT(IN), OPTIONAL :: FMT
      CHARACTER(:),ALLOCATABLE :: out_fmt
      
      IF (PRESENT(fmt)) THEN
        out_fmt = trim(fmt)
      ELSE
        out_fmt = 'F8.3'
      END IF
      
      write(*,'(A)',advance='no') "["
      
      DO I=1,SIZE(X,1)
        
        IF (I == 1) THEN
          write(*,'(A)',advance='no') "["
        ELSE
          write(*,'(A)',advance='no') " ["
        END IF 

        DO J=1,SIZE(X,2)
          
          write(*,'('//trim(out_fmt)//')',advance='no') X(I,J)

        END DO
        
        write(*,'(A)') "]"
      
      END DO

      write(*,'(A)') "]"
      

  END SUBROUTINE MDNUM3_PPRINT_M_R
