  END FUNCTION ONUMM1N3_MUL_Ro

  ELEMENTAL FUNCTION ONUMM1N3_MUL_oR(LHS,RHS)&
    RESULT(RES)
    IMPLICIT NONE
    TYPE(ONUMM1N3), INTENT(IN) :: LHS 
    REAL(DP), INTENT(IN) :: RHS 
