  END FUNCTION ONUMM1N3_MUL_oR

  FUNCTION ONUMM1N3_MATMUL_ONUMM1N3(LHS,RHS)&
    RESULT(RES)
    IMPLICIT NONE
    TYPE(ONUMM1N3), INTENT(IN) :: LHS(:,:)
    TYPE(ONUMM1N3), INTENT(IN) :: RHS(:,:)
    TYPE(ONUMM1N3) :: RES(SIZE(LHS,1),SIZE(RHS,2))

    !  Multiplication like function 'MATMUL(LHS,RHS)'
    ! Real
    RES%R = MATMUL(LHS%R,RHS%R)
    ! Order 1
    RES%E1 = MATMUL(LHS%R,RHS%E1) + MATMUL(LHS%E1,RHS%R)
    ! Order 2
    RES%E11 = MATMUL(LHS%R,RHS%E11) + MATMUL(LHS%E11,RHS%R) + MATMUL(LHS%E1,RHS%E1)
    ! Order 3
    RES%E111 = MATMUL(LHS%R,RHS%E111) + MATMUL(LHS%E111,RHS%R) + MATMUL(LHS%E1,RHS%E11) &
             + MATMUL(LHS%E11,RHS%E1)

  END FUNCTION ONUMM1N3_MATMUL_ONUMM1N3

  FUNCTION R_MATMUL_ONUMM1N3(LHS,RHS)&
    RESULT(RES)
    IMPLICIT NONE
    REAL(DP), INTENT(IN) :: LHS(:,:)
    TYPE(ONUMM1N3), INTENT(IN) :: RHS(:,:)
    TYPE(ONUMM1N3) :: RES(SIZE(LHS,1),SIZE(RHS,2))

    ! Multiplication like function 'MATMUL(LHS,RHS)'
    !  Real
    RES%R = MATMUL(LHS,RHS%R)
    ! Order 1
    RES%E1 = MATMUL(LHS,RHS%E1)
    ! Order 2
    RES%E11 = MATMUL(LHS,RHS%E11)
    ! Order 3
