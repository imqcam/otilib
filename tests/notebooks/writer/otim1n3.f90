  END FUNCTION ONUMM1N3_MATMUL_R

  FUNCTION ONUMM1N3_TRANSPOSE(LHS)&
    RESULT(RES)
    IMPLICIT NONE
    TYPE(ONUMM1N3), INTENT(IN) :: LHS(:,:)
    TYPE(ONUMM1N3) :: RES (SIZE(LHS,2),SIZE(LHS,1))

