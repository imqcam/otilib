coeff_t fecnum_integrate(fecnum_t* num, fecnum_t* w){

    coeff_t  res;

    fecnum_integrate_to(num, w, &res);

    return res;

}
// ----------------------------------------------------------------------------------------------------

// ****************************************************************************************************
void fecnum_integrate_to(fecnum_t* num, fecnum_t* w, coeff_t* res){

    uint64_t i = 0;
    coeff_t tmp = 0.0;

    // Check dimensions.
    fecnum_dimCheck( num, w);

    // The loop for every element in num.
    for (i = 0; i < num->nip; i++){
        
        tmp += w->p_data[i] * num->p_data[i];

    }

    (*res) = tmp;
}
