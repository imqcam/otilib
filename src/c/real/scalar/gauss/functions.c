coeff_t fednum_integrate_r(coeff_t num, fednum_t* w){

    coeff_t  res = 0.0;

    fednum_integrate_r_to(num, w, &res);

    return res;

}
// ----------------------------------------------------------------------------------------------------

// ****************************************************************************************************
void fednum_integrate_r_to(coeff_t num, fednum_t* w, coeff_t* res){

    uint64_t i = 0;

    coeff_t tmp = 0.0;

    // The loop for every element in num.
    for (i = 0; i < w->nip; i++){
        
        tmp += w->p_data[i];

    }

    (*res) = num * tmp;

}
