coeff_t fednum_integrate(fednum_t* num, fednum_t* w){

    coeff_t  res;

    fednum_integrate_to(num, w, &res);

    return res;

}
// ----------------------------------------------------------------------------------------------------

// ****************************************************************************************************
void fednum_integrate_to(fednum_t* num, fednum_t* w, coeff_t* res){

    uint64_t i = 0;
    coeff_t tmp = 0.0;

    // Check dimensions.
    fednum_dimCheck( num, w);

    // The loop for every element in num.
    for (i = 0; i < num->nip; i++){
        
        tmp += w->p_data[i] * num->p_data[i];

    }

    (*res) = tmp;
}
