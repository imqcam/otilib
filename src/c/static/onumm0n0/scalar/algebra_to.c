void onumm0n0_div_ro_to(coeff_t num, onumm0n0_t* den, onumm0n0_t* res){

    onumm0n0_t inv;
    onumm0n0_pow_to( den, -1, &inv);
    onumm0n0_mul_ro_to(num,&inv,res);

}
