// ****************************************************************************************************
void onumm1n9_abs_to(onumm1n9_t* num, onumm1n9_t* res){

	if (num->r < 0){
		onumm1n9_neg_to(num,res);
	} else {
		onumm1n9_copy_to(num,res);
	}

}
